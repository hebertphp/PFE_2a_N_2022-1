var destino;
destino=prompt("Digite seu destino: ");

switch (destino) {
    case "São Paulo":
        document.write("Seja bem vindo a cidade que nunca para!");
    break;
    case "Rio de Janeiro":
        document.write("Seja bem vindo a cidade maravilhosa!");
    break;
    case "New York":
        document.write("Seja bem vindo a Big Apple!");
    break;
    default:
        document.write("Não encontramos seu destino!");
}
/*
1 - Consulta
2 - Receita
3 - Falar
4 - Escutar
0 - Sair
*/